function buildOps(index, key, districtMap) {
  const ops = [];
  for (const [district, values] of Object.entries(districtMap)) {
    for (let i = 0; i < dates.length; i++) {
      const compositeDate = `${dates[i]}/${district}`;
      ops.push({
        updateOne: {
          filter: { index, date: compositeDate },
          update: { $set: { value: values[i], key } },
          upsert: true,
        },
      });
    }
  }
  return ops;
}

